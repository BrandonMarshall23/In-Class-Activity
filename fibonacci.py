def fib2(n): # return Fibonacci series up to n
    result = []
    a, b = 0, 1
    while b < n:
        result.append(b)
        a, b = b, a+b
    return result
    
def factorial(n):
    if n == 0:
        return 1
    else:
        return n * factorial(n-1)
